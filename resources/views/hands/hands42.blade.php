العامل الروماتويدي: هو جسم مضاد يتم اكتشافه عند حوالي 70% من الاشخاص الذين يعانون من التهاب المفاصل الروماتويدي rheumatoid arthritis، لكن يمن الممكن ان يصاب به اشخاص اخرين. وفي الحقيقة، ينتج الاشخاص الذين لا يعانون من التهاب المفاصل الروماتويدي، او اي مشكلات طبية اخرى كميات ضئيلة من هذا الجسم المضاد. وعند 90-95% من الاشخاص الاصحاء صغار السن، تكون كمية العامل الروماتويدي الموجودة في الدم منخفضة جدا، لدرجة انها تعتبر منعدمة من الدم، ويعتبر الاختبار سلبيا (النتيجة الطبيعية).

ويرتفع عدد الاشخاص الذين يعانون من عامل روماتويدي ايجابي الى حد ما بتقدم السن، بالرغم من ان الاشخاص المسنين المرضى يميلون الى ان يكون لديهم عامل روماتويدي ايجابي، اكثر من المسنين الاصحاء. وعندما يكون العامل الروماتويدي ايجابيا ايضا، دون مرض موجود، يكون مرتفعا قليلا عادة، بينما تكون المستويات الاعلى محتملة، اشارة الى مرض حقيقي من نوع اخر. انظر في الاسفل من اجل الحالات التي يكون فيها العامل الروماتويدي ايجابيا، غير التهاب المفاصل الروماتويدي.

الحالات الاخرى التي يصاحبها التهاب في المفاصل ربما تؤدي الى عامل روماتويدي ايجابي: متلازمة جوجرين Sjogren's syndrome، والذئبة الحمراء systemic lupus erythematosus، هما مثالان جيدان، لكن هاتين الحالتين عادة تسببان اعراضا اخرى، ومشكلات في اختبارات الدم تقدم مفاتيح للحلول.

